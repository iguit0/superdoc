const PYTHON_GENERATED_DIR = path.join(REPO_ROOT, 'packages/sdk/langs/python/superdoc/generated');

// ---------------------------------------------------------------------------
// contract.py — embedded contract dict
// ---------------------------------------------------------------------------

function generateContractPy(contract) {
  const payload = JSON.stringify(contract);
  return [
    '# Auto-generated by packages/sdk/codegen/src/generate-python.mjs',
    '',
    'import json',
    '',
    `CONTRACT = json.loads(${JSON.stringify(payload)})`,
    '',
    'OPERATION_INDEX = {op_id: op for op_id, op in CONTRACT["operations"].items()}',
    '',
  ].join('\n');
}

// ---------------------------------------------------------------------------
// Python type mapping
// ---------------------------------------------------------------------------

function toPythonLiteral(value) {
  if (typeof value === 'string') return JSON.stringify(value);
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return value ? 'True' : 'False';
  return 'None';
}

function toPythonType(typeSpec, classNameBase, generatedClasses, classBlocks, $defs = undefined) {
  if (!typeSpec) return 'Any';
  typeSpec = resolveRef(typeSpec, $defs);

  if (Object.prototype.hasOwnProperty.call(typeSpec, 'const')) {
    return `Literal[${toPythonLiteral(typeSpec.const)}]`;
  }

  if (Array.isArray(typeSpec.oneOf)) {
    const variants = typeSpec.oneOf.map((v, i) =>
      toPythonType(v, `${classNameBase}Variant${i + 1}`, generatedClasses, classBlocks, $defs),
    );
    return variants.join(' | ');
  }

  if (typeSpec.enum && Array.isArray(typeSpec.enum)) {
    return typeSpec.enum.map((v) => `Literal[${toPythonLiteral(v)}]`).join(' | ');
  }

  switch (typeSpec.type) {
    case 'string':
      return 'str';
    case 'number':
    case 'integer':
      return 'int | float';
    case 'boolean':
      return 'bool';
    case 'null':
      return 'None';
    case 'json':
      return 'Any';
    case 'array':
      return `list[${toPythonType(typeSpec.items, `${classNameBase}Item`, generatedClasses, classBlocks, $defs)}]`;
    case 'object': {
      const className = classNameBase;
      if (!generatedClasses.has(className)) {
        generatedClasses.add(className);
        const lines = [`${className} = TypedDict(`, `    ${JSON.stringify(className)},`, '    {'];
        const properties = Object.entries(typeSpec.properties ?? {});
        if (properties.length === 0) {
          lines.push('    },');
          lines.push('    total=False,');
          lines.push(')');
        } else {
          for (const [name, propSpec] of properties) {
            const propType = toPythonType(
              propSpec,
              `${className}${pascalCase(name)}`,
              generatedClasses,
              classBlocks,
              $defs,
            );
            lines.push(`        ${JSON.stringify(name)}: ${propType},`);
          }
          lines.push('    },');
          lines.push('    total=False,');
          lines.push(')');
        }
        classBlocks.push(lines.join('\n'));
      }
      return className;
    }
    default:
      return 'Any';
  }
}

// ---------------------------------------------------------------------------
// Param type spec helpers
// ---------------------------------------------------------------------------

function paramTypeSpec(param) {
  if (param.type === 'string') return { type: 'string' };
  if (param.type === 'number') return { type: 'number' };
  if (param.type === 'boolean') return { type: 'boolean' };
  if (param.type === 'string[]') return { type: 'array', items: { type: 'string' } };
  if (param.type === 'json' && param.schema) return param.schema;
  return { type: 'json' };
}

function buildParamsObjectSpec(operation) {
  const properties = {};
  const required = [];

  for (const param of operation.params ?? []) {
    properties[param.name] = paramTypeSpec(param);
    if (param.required) required.push(param.name);
  }

  return { type: 'object', properties, required };
}

function buildBoundParamsObjectSpec(operation) {
  const properties = {};
  const required = [];

  for (const param of stripBoundParams(operation.params)) {
    properties[param.name] = paramTypeSpec(param);
    if (param.required) required.push(param.name);
  }

  return { type: 'object', properties, required };
}

// ---------------------------------------------------------------------------
// Response and param type generation
// ---------------------------------------------------------------------------

function generateResponseTypes(operations, generatedClasses, classBlocks, $defs) {
  const map = new Map();

  for (const [operationId, operation] of Object.entries(operations)) {
    const resultClassName = `Doc${pascalCase(sanitizeOperationId(operationId))}Result`;
    const schema = operation.successSchema ?? operation.outputSchema;
    if (!schema) {
      throw new Error(`Operation ${operationId} missing both successSchema and outputSchema`);
    }
    map.set(operationId, toPythonType(schema, resultClassName, generatedClasses, classBlocks, $defs));
  }

  return map;
}

function generateParamTypes(operations, generatedClasses, classBlocks, $defs) {
  const map = new Map();

  for (const [operationId, operation] of Object.entries(operations)) {
    const paramsClassName = `Doc${pascalCase(sanitizeOperationId(operationId))}Params`;
    const paramsType = toPythonType(
      buildParamsObjectSpec(operation),
      paramsClassName,
      generatedClasses,
      classBlocks,
      $defs,
    );
    map.set(operationId, paramsType);
  }

  return map;
}

// ---------------------------------------------------------------------------
// Class generation
// ---------------------------------------------------------------------------

function classNameFor(pathParts, asyncMode) {
  const prefix = asyncMode ? '_Async' : '_Sync';
  return `${prefix}${pathParts.map((p) => pascalCase(p)).join('')}Api`;
}

// String-typed envelope keys that should use _unwrap_string_envelope instead of _unwrap_envelope.
const STRING_ENVELOPE_KEYS = new Set(['text', 'markdown', 'html']);

function snakeCase(value) {
  return value
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1_$2')
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/[^a-zA-Z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .toLowerCase();
}

function renderClass(treeNode, pathParts, asyncMode, resultTypeMap, paramTypeMap) {
  const className = classNameFor(pathParts, asyncMode);
  const lines = [`class ${className}:`, '    def __init__(self, runtime):', '        self._runtime = runtime', ''];

  for (const [key, value] of Object.entries(treeNode)) {
    if (value.__operation) {
      const methodName = snakeCase(key);
      const camelAlias = camelCase(key);
      const operationId = value.__operation.id;
      const resultType = resultTypeMap.get(operationId) ?? 'dict[str, Any]';
      const paramsType = paramTypeMap.get(operationId) ?? 'dict[str, Any]';
      const envelopeKey = value.__operation.responseEnvelopeKey ?? null;
      const stringEnvelopeKey = resultType === 'str' && envelopeKey && STRING_ENVELOPE_KEYS.has(envelopeKey) ? envelopeKey : null;
      const invokeExpression = `${asyncMode ? 'await ' : ''}self._runtime.invoke(${JSON.stringify(operationId)}, params or {}, timeout_ms=timeout_ms, stdin_bytes=stdin_bytes)`;

      if (asyncMode) {
        lines.push(
          `    async def ${methodName}(self, params: ${paramsType} | None = None, *, timeout_ms=None, stdin_bytes=None) -> ${resultType}:`,
        );
        if (stringEnvelopeKey) {
          lines.push(`        raw = ${invokeExpression}`);
          lines.push(`        return _unwrap_string_envelope(raw, ${JSON.stringify(stringEnvelopeKey)})`);
        } else if (envelopeKey) {
          lines.push(`        raw = ${invokeExpression}`);
          lines.push(`        return _unwrap_envelope(raw, ${JSON.stringify(envelopeKey)})`);
        } else {
          lines.push(`        return ${invokeExpression}`);
        }
      } else {
        lines.push(
          `    def ${methodName}(self, params: ${paramsType} | None = None, *, timeout_ms=None, stdin_bytes=None) -> ${resultType}:`,
        );
        if (stringEnvelopeKey) {
          lines.push(`        raw = ${invokeExpression}`);
          lines.push(`        return _unwrap_string_envelope(raw, ${JSON.stringify(stringEnvelopeKey)})`);
        } else if (envelopeKey) {
          lines.push(`        raw = ${invokeExpression}`);
          lines.push(`        return _unwrap_envelope(raw, ${JSON.stringify(envelopeKey)})`);
        } else {
          lines.push(`        return ${invokeExpression}`);
        }
      }
      lines.push('');

      if (camelAlias !== methodName) {
        if (asyncMode) {
          lines.push(
            `    async def ${camelAlias}(self, params: ${paramsType} | None = None, *, timeout_ms=None, stdin_bytes=None) -> ${resultType}:`,
          );
          lines.push(`        return await self.${methodName}(params, timeout_ms=timeout_ms, stdin_bytes=stdin_bytes)`);
        } else {
          lines.push(
            `    def ${camelAlias}(self, params: ${paramsType} | None = None, *, timeout_ms=None, stdin_bytes=None) -> ${resultType}:`,
          );
          lines.push(`        return self.${methodName}(params, timeout_ms=timeout_ms, stdin_bytes=stdin_bytes)`);
        }
        lines.push('');
      }
      continue;
    }

    const propertyName = snakeCase(key);
    const camelAlias = camelCase(key);
    const nestedClassName = classNameFor([...pathParts, key], asyncMode);
    lines.push('    @property');
    lines.push(`    def ${propertyName}(self):`);
    lines.push(`        return ${nestedClassName}(self._runtime)`);
    lines.push('');

    if (camelAlias !== propertyName) {
      lines.push('    @property');
      lines.push(`    def ${camelAlias}(self):`);
      lines.push(`        return self.${propertyName}`);
      lines.push('');
    }
  }

  return lines.join('\n').trimEnd();
}

function renderAllClasses(treeNode, pathParts, asyncMode, resultTypeMap, paramTypeMap) {
  const blocks = [renderClass(treeNode, pathParts, asyncMode, resultTypeMap, paramTypeMap)];

  for (const [key, value] of Object.entries(treeNode)) {
    if (value.__operation) continue;
    blocks.push('');
    blocks.push(renderAllClasses(value, [...pathParts, key], asyncMode, resultTypeMap, paramTypeMap));
  }

  return blocks.join('\n');
}

// ---------------------------------------------------------------------------
// client.py generation
// ---------------------------------------------------------------------------

function generateBoundParamTypes(operations, generatedClasses, classBlocks, $defs) {
  const map = new Map();

  for (const [operationId, operation] of Object.entries(operations)) {
    const paramsClassName = `Doc${pascalCase(sanitizeOperationId(operationId))}BoundParams`;
    const paramsType = toPythonType(
      buildBoundParamsObjectSpec(operation),
      paramsClassName,
      generatedClasses,
      classBlocks,
      $defs,
    );
    map.set(operationId, paramsType);
  }

  return map;
}

function generateClientPy(contract) {
  const $defs = contract.$defs;
  const allOperations = contract.operations;
  const documentOperations = filterOperationsBySurface(allOperations, 'document');
  const generatedClasses = new Set();
  const classBlocks = [];

  // Raw types for all operations (internal)
  const resultTypeMap = generateResponseTypes(allOperations, generatedClasses, classBlocks, $defs);
  const paramTypeMap = generateParamTypes(allOperations, generatedClasses, classBlocks, $defs);

  // Bound types for document-surface operations (public)
  const boundParamTypeMap = generateBoundParamTypes(documentOperations, generatedClasses, classBlocks, $defs);

  const sharedTypes = classBlocks.join('\n\n');

  // Raw operation trees (all operations — internal)
  const rawTree = createOperationTree(allOperations);
  const rawSyncClasses = renderAllClasses(rawTree, ['doc'], false, resultTypeMap, paramTypeMap);
  const rawAsyncClasses = renderAllClasses(rawTree, ['doc'], true, resultTypeMap, paramTypeMap);

  // Bound operation trees (document-surface only — public)
  const boundTree = createOperationTree(documentOperations);
  const boundSyncClasses = renderAllClasses(boundTree, ['bound', 'doc'], false, resultTypeMap, boundParamTypeMap);
  const boundAsyncClasses = renderAllClasses(boundTree, ['bound', 'doc'], true, resultTypeMap, boundParamTypeMap);

  return [
    '# Auto-generated by packages/sdk/codegen/src/generate-python.mjs',
    '# Operation tree and TypedDicts only — lifecycle classes live in client.py.',
    '',
    'from __future__ import annotations',
    '',
    'from typing import Any, Literal, TypedDict, cast',
    '',
    'def _unwrap_envelope(value: Any, key: str) -> Any:',
    '    if isinstance(value, dict) and key in value:',
    '        return value[key]',
    '    return value',
    '',
    'def _unwrap_string_envelope(value: Any, key: str) -> str:',
    '    extracted = _unwrap_envelope(value, key)',
    '    if isinstance(extracted, str):',
    '        return extracted',
    '    return cast(str, extracted)',
    '',
    sharedTypes,
    '',
    '# ---------------------------------------------------------------------------',
    '# Raw API (all operations — internal use only)',
    '# ---------------------------------------------------------------------------',
    '',
    rawSyncClasses,
    '',
    rawAsyncClasses,
    '',
    '# ---------------------------------------------------------------------------',
    '# Bound API (document-surface only — public document handle)',
    '# ---------------------------------------------------------------------------',
    '',
    boundSyncClasses,
    '',
    boundAsyncClasses,
    '',
  ].join('\n');
}

// ---------------------------------------------------------------------------
// Entry
// ---------------------------------------------------------------------------

export async function generatePythonSdk(contract) {
  const contractContent = generateContractPy(contract);
  const clientContent = generateClientPy(contract);

  await Promise.all([
    writeGeneratedFile(path.join(PYTHON_GENERATED_DIR, 'contract.py'), contractContent),
    writeGeneratedFile(path.join(PYTHON_GENERATED_DIR, 'client.py'), clientContent),
    writeGeneratedFile(
      path.join(PYTHON_GENERATED_DIR, '__init__.py'),
      [
        'from .client import _SyncDocApi, _AsyncDocApi',
        'from .client import _SyncBoundDocApi, _AsyncBoundDocApi',
        '',
      ].join('\n'),
    ),
  ]);
}

if (import.meta.url.endsWith(process.argv[1]?.replace(/^file:\/\//, '') ?? '')) {
  const contract = await loadContract();
  await generatePythonSdk(contract);
  console.log('Generated Python SDK files.');
}
